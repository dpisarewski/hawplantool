package de.dueddel.hawplantool;

/**
 * <code>HAWPlanToolException</code>
 */
public class HAWPlanToolException extends Exception {

	/**
	 * Konstruktor. Erzeugt eine Instanz der Klasse <code>HAWPlanToolException</code>.
	 */
	public HAWPlanToolException(String message) {
		super(message);
	}

	/**
	 * Konstruktor. Erzeugt eine Instanz der Klasse <code>HAWPlanToolException</code>.
	 */
	public HAWPlanToolException(String message, Throwable cause) {
		super(message, cause);
	}
}